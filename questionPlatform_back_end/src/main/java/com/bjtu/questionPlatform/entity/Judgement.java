package com.bjtu.questionPlatform.entity;

public class Judgement {
    String judgementid;
    String judgementcontent;
    String managerid;
    String judgementproportion;
    String judgementname;

    public String getJudgementid() {
        return judgementid;
    }

    public void setJudgementid(String judgementid) {
        this.judgementid = judgementid;
    }

    public String getJudgementcontent() {
        return judgementcontent;
    }

    public void setJudgementcontent(String judgementcontent) {
        this.judgementcontent = judgementcontent;
    }

    public String getManagerid() {
        return managerid;
    }

    public void setManagerid(String managerid) {
        this.managerid = managerid;
    }

    public String getJudgementproportion() {
        return judgementproportion;
    }

    public void setJudgementproportion(String judgementproportion) {
        this.judgementproportion = judgementproportion;
    }

    public String getJudgementname() {
        return judgementname;
    }

    public void setJudgementname(String judgementname) {
        this.judgementname = judgementname;
    }
}
